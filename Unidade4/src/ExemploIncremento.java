public class ExemploIncremento {
    public static void main(String[] args) {
        int x = 0;
        System.out.println(x);
        x++;
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);
        System.out.println(++x);
        ++x;
        System.out.println(x);
    }
}
